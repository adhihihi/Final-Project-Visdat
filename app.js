function countUp(id, target, opts = {}) {
    const el = document.getElementById(id);
    if (!el) return;

    const {
        isFloat   = false,
        decimals  = 3,
        prefix    = '',
        suffix    = '',
        duration  = 1800,
    } = opts;

    const steps     = 60;
    const increment = target / steps;
    let current     = 0;
    let step        = 0;

    const timer = setInterval(() => {
        step++;
        current += increment;
        if (step >= steps) {
            current = target;
            clearInterval(timer);
        }
        const display = isFloat
            ? current.toFixed(decimals)
            : Math.floor(current).toString();
        el.textContent = prefix + display + suffix;
    }, duration / steps);
}

