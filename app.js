// ============================================================
//  INTERACTIVE TIMELINE & SIMULATION CHART
// ============================================================
function initTimelineChart() {
    const ctxEl = document.getElementById('timelineChart');
    if (!ctxEl) return;

    // State variables
    let isPlaying = false;
    let animInterval = null;
    let activePeriod = 'all'; // 'all', '10', '5'
    let startYear = 2005;
    const endYear = 2023;

    // Ambil daftar tahun unik dari trend data
    const fullYears = [...new Set(
        Object.values(DATA.trend).flatMap(arr => arr.map(d => d.year))
    )].sort((a, b) => a - b);

    // Ambil daftar negara yang sedang dicentang
    function getSelectedCountries() {
        return Array.from(document.querySelectorAll('.country-chk:checked')).map(el => el.value);
    }

    // Tentukan tahun target untuk ditampilkan
    function getTargetYears() {
        return fullYears.filter(yr => yr >= startYear && yr <= endYear);
    }

    // Palette warna untuk negara
    const palette = {
        'Indonesia':     { line: '#f87171', fill: 'rgba(248,113,113,0.08)' },
        'Finland':       { line: '#fbbf24', fill: 'rgba(251,191,36,0.08)'  },
        'Singapore':     { line: '#2dd4bf', fill: 'rgba(45,212,191,0.08)'  },
        'World Average': { line: '#8ba3c1', fill: 'rgba(139,163,193,0.05)' }
    };

    // Susun dataset Chart.js berdasarkan daftar tahun yang akan digambar
    function buildDatasets(displayYears) {
        const countries = getSelectedCountries();
        return countries.map(country => {
            const trendData = DATA.trend[country] || [];
            const scoreByYear = Object.fromEntries(trendData.map(d => [d.year, d.score]));
            const col = palette[country] || { line: '#8ba3c1', fill: 'transparent' };

            return {
                label: country === 'World Average' ? 'Rata-Rata Dunia' : (country === 'Finland' ? 'Finlandia' : (country === 'Singapore' ? 'Singapura' : country)),
                data: displayYears.map(yr => scoreByYear[yr] ?? null),
                borderColor: col.line,
                backgroundColor: col.fill,
                borderWidth: country === 'Indonesia' ? 3.5 : 2.5,
                pointRadius: displayYears.length <= 1 ? 6 : (country === 'Indonesia' ? 3.5 : 2),
                pointHoverRadius: 8,
                pointBackgroundColor: col.line,
                tension: 0.3,
                spanGaps: true,
                fill: false,
            };
        });
    }

    // Inisialisasi Chart.js
    const displayYears = getTargetYears();
    const chart = new Chart(ctxEl, {
        type: 'line',
        data: {
            labels: displayYears,
            datasets: buildDatasets(displayYears)
        },
        options: {
            responsive: true,
            maintainAspectRatio: false,
            animation: {
                duration: 800,
                easing: 'easeOutQuart'
            },
            interaction: { mode: 'index', intersect: false },
            plugins: {
                legend: { display: false }, // Legenda ditangani secara manual menggunakan checkbox di atas
                tooltip: {
                    callbacks: {
                        label: ctx => ` ${ctx.dataset.label}: ${ctx.parsed.y != null ? ctx.parsed.y.toFixed(3) : 'N/A'}`
                    }
                }
            },
            scales: {
                x: {
                    grid: { color: '#1e3048' },
                    ticks: { color: '#8ba3c1' }
                },
                y: {
                    min: 3.0,
                    max: 8.5,
                    grid: { color: '#1e3048' },
                    ticks: { color: '#8ba3c1' },
                    title: {
                        display: true,
                        text: 'Skor Kebahagiaan',
                        color: '#4e6880',
                        font: { size: 11 }
                    }
                }
            }
        }
    });

    // Update grafik
    function updateChart(animate = true) {
        const displayYears = getTargetYears();
        chart.data.labels = displayYears;
        chart.data.datasets = buildDatasets(displayYears);
        chart.options.animation.duration = animate ? 800 : 0;
        chart.update();
    }

    // Set nilai select dropdown tahun mulai
    function setDropdownValue(year) {
        const select = document.getElementById('start-year-select');
        if (select) {
            select.value = year.toString();
        }
    }

    // Event handler untuk tombol preset periode
    const periodButtons = document.querySelectorAll('.period-btn');
    periodButtons.forEach(btn => {
        btn.addEventListener('click', () => {
            stopAnimation();

            periodButtons.forEach(b => b.classList.remove('active'));
            btn.classList.add('active');

            activePeriod = btn.getAttribute('data-period');
            if (activePeriod === 'all') {
                startYear = 2005;
            } else if (activePeriod === '10') {
                startYear = 2013;
            } else if (activePeriod === '5') {
                startYear = 2018;
            }
            setDropdownValue(startYear);
            updateChart();
        });
    });

    // Event handler untuk perubahan select dropdown tahun mulai
    const startSelect = document.getElementById('start-year-select');
    if (startSelect) {
        startSelect.addEventListener('change', (e) => {
            stopAnimation();
            startYear = parseInt(e.target.value);

            // Sesuaikan kelas aktif pada preset tombol periode
            periodButtons.forEach(b => {
                const p = b.getAttribute('data-period');
                if ((p === 'all' && startYear === 2005) ||
                    (p === '10' && startYear === 2013) ||
                    (p === '5' && startYear === 2018)) {
                    b.classList.add('active');
                } else {
                    b.classList.remove('active');
                }
            });

            updateChart();
        });
    }

    // Event handler untuk checkbox negara
    const checkboxes = document.querySelectorAll('.country-chk');
    checkboxes.forEach(chk => {
        chk.addEventListener('change', () => {
            if (!isPlaying) {
                updateChart(true);
            } else {
                // Jika sedang berputar, sesuaikan dataset saja untuk tahun berjalan
                const displayYears = fullYears.filter(yr => yr >= startYear && yr <= currentAnimYear);
                chart.data.datasets = buildDatasets(displayYears);
                chart.update('none');
            }
        });
    });

    // Simulasi Animasi Linimasa (Play / Pause)
    let currentAnimYear = 2005;
    const playBtn = document.getElementById('btn-play-timeline');

    function stopAnimation() {
        if (animInterval) {
            clearInterval(animInterval);
            animInterval = null;
        }
        isPlaying = false;
        if (playBtn) {
            playBtn.innerHTML = '<span class="play-icon">▶</span> Putar Animasi';
            playBtn.className = 'control-btn control-btn--play';
        }
    }

    function startAnimation() {
        isPlaying = true;
        if (playBtn) {
            playBtn.innerHTML = '<span class="play-icon">⏸</span> Jeda Animasi';
            playBtn.className = 'control-btn control-btn--pause';
        }

        const targetYears = getTargetYears();
        let idx = 0;
        
        // Reset grafik ke tahun pertama saja
        currentAnimYear = targetYears[0];
        chart.data.labels = [currentAnimYear];
        chart.data.datasets = buildDatasets([currentAnimYear]);
        chart.options.animation.duration = 0;
        chart.update();

        // Ticker animasi menambahkan data tahun demi tahun
        animInterval = setInterval(() => {
            idx++;
            if (idx >= targetYears.length) {
                stopAnimation();
                updateChart(true);
                return;
            }

            currentAnimYear = targetYears[idx];
            const activeYears = targetYears.slice(0, idx + 1);

            chart.data.labels = activeYears;
            chart.data.datasets = buildDatasets(activeYears);
            chart.options.animation.duration = 0; // update tanpa animasi default agar mulus tergambar
            chart.update('none');
        }, 220);
    }

    if (playBtn) {
        playBtn.addEventListener('click', () => {
            if (isPlaying) {
                stopAnimation();
                updateChart(true);
            } else {
                startAnimation();
            }
        });
    }
}

